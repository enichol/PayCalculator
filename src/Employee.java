public class Employee {
    String firstName;
    String lastName;
    String residentState;
    String department;
    String role;

    public Employee (String firstName, String lastName, String residentState, String department, String role) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.residentState = residentState;
        this.department = department;
        this.role = role;
    }
}
